#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int k;

	k = 0;
	while (s[k] != '\0')
	{
		if (s[k] == c)
			return ((char *)s);
		k++;
	}
	if (c == 0)
		return (char *)s;
	return (0);
}
