#include "libft.h"
#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	char *s1;

	s1 = (char*)s;
	if (n == 0)
		return (*s);
	while (n > 0)
	{
		*s1 = '\0';
		s1++;
		n--;
	}
	return (*s);
}
