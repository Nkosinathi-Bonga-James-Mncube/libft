#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int k;
	int j;

	k = 0;
	j = 0;
	while (s1[k] != '\0')
		k++;
	while (s2[j] != '\0')
	{
		s1[k] = s2[j];
		j++;
		k++;
	}
	s1[k] = '\0';
	return (s1);
}
