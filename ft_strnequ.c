#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int k;

	k = 0;
	if (!s1 || !s2 )
		return (0);
	while (n > 0 && (s1[k] != '\0' || s2[k] != '\0'))
	{
		if (s1[k] == s2[k])
			k++;
		else
			return (0);
		n--;
	}
	return (1);
}
