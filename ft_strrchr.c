#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int k;

	k = ft_strlen(s);
	while (k >= 0)
	{
		if (s[k] == c)
			return ((char*)&s[k]);
		k--;
	}
	return (0);
}
