#include "libft.h"

char	*ft_strcpy(char *dst, const char *scr)
{
	int		k;
	char	*s1;

	k = 0;
	s1 = (char*)scr;
	while (s1[k] != '\0')
	{
		dst[k] = s1[k];
		k++;
	}
	return (dst);
}
